function randomBlockOrItem(){
	return ids[randomToInt(ids.length)];
	//return randomString(randomRangeInt(2, 5))+"."+randomString(randomRangeInt(3, 25));
}

//Level 2 (minor components: items, blocks, drops, stacks)
function randomItemData(){
	var data = randomBlockOrItem();
	return [data, data];
}
function randomStack(){
	var data = randomBlockOrItem();
	var amt = randomRangeInt(1, 100);
	return [data, data, amt];
}
function randomDrop(){
	return [
	randomRangeInt(-100, 100),
	randomRangeInt(-100, 100),
	randomItemData()
	];
}

//Level 1 (major components: inventories, world data, drop data)
function randomWorldData(){
	return repeat(2500, randomBlockOrItem);
}
function randomDrops(){
	return repeat(300, randomDrop);
}
function randomInventory(){
	var data = repeat(randomRangeInt(1, 100), randomStack);
	data.unshift(randomRange(1, 256));
	return data;
}

//Level 0 (worlds)
function randomWorld(){
	return {
		sizeX: 50,
		sizeY: 50,
		startX: randomRangeInt(-100, 100),
		startY: randomRangeInt(-100, 100),
		world: randomWorldData(),
		drops: randomDrops(),
		machines: [],
		data: {},
		player:{
			inventory: randomInventory(),
			creative: randomBool(),
			x: randomRange(-100, 100),
			y: randomRange(-100, 100),
			alcohol1: randomRange(0, 5),
			alcohol2: randomRange(0, 5)
		}
	};
}
function randomUniverse(){
	return{
		main: randomWorld(),
		data: {},
		maps: {}
	};
}

